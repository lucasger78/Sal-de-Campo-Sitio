// ver más original
document.getElementById('ver-mas-entrefina').addEventListener('click', function(event) {
    event.preventDefault();
    document.getElementById('ingredientes-preview-entrefina').style.display = 'none'; 
    document.getElementById('ingredientes-completos-entrefina').style.display = 'inline';
    this.style.display = 'none'; 
});

